package myPackage;

public class Box 
{
	public static void main(String[] args)
	{
		//Print a name enclosed in a box.
		System.out.println("+----------+");
		System.out.println("|Antoinette|");
		System.out.println("+----------+");
	
		//Prints a text-based face.
		System.out.println(" + + + +"); //This prints the hair.
		System.out.println(" | 0  0|"); //This prints the eyes.
		System.out.println("q|   v |p"); //This prints the ears and nose.
		System.out.println(" | ----|"); //This prints the mouth.
		System.out.println("  ----- "); //This prints the bottom of the face.
		
		//Prints out a tic-tac-toe board.
		System.out.println("+---+---+---+");
		System.out.println("| x | o |   |");
		System.out.println("+---+---+---+");
		System.out.println("|   | x |   |");
		System.out.println("+---+---+---+");
		System.out.println("| o | o | x |");
		System.out.println("+---+---+---+");
		
		//Prints a staircase.
		System.out.println("            +---+");
		System.out.println("            |   |");
		System.out.println("        +---+---+");
		System.out.println("        |   |   |");
		System.out.println("    +---+---+---+");
		System.out.println("    |   |   |   |");
		System.out.println("+---+---+---+---+");
		System.out.println("|   |   |   |   |");
		System.out.println("+---+---+---+---+");
		
	}
}
